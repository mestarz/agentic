#!/bin/bash

# 获取脚本所在目录的绝对路径
SCRIPT_DIR=$(cd "$(dirname "$0")"; pwd)

echo "---------------------------------------"
echo "🔄 正在重启 ContextFabric 全栈服务..."
echo "---------------------------------------"

# 执行停止脚本
bash "$SCRIPT_DIR/stop.sh"

# 稍微等待一下确保端口释放
sleep 2

# 执行启动脚本
bash "$SCRIPT_DIR/start.sh"

echo "---------------------------------------"
echo "✅ 重启任务已完成！"
echo "---------------------------------------"
